package day20;

public class Acceleration {

  private long x;
  private long y;
  private long z;

  public Acceleration(long x, long y, long z) {
    this.x = x;
    this.y = y;
    this.z = z;
  }

  public long getX() {
    return x;
  }

  public long getY() {
    return y;
  }

  public long getZ() {
    return z;
  }
}
